func AppendSlice(b []string) string {
	b = append(b[:0], b[1:]...)
	return strings.Join(b, "")
}

func CopySlice(b []string) string {
	copy(b[0:], b[1:])
	return strings.Join(b[:len(b)-1], "")
}
func ForiSlice(b []string) string {
	b = b[1:]
	return strings.Join(b, "")
}

